def apply_asia_ceiling(  # 定義函式，用來模擬中油的「亞鄰競爭國最低價限制」政策
    pred_price: float,  # 參數一：傳入模型預測出的原始油價（浮點數）
    oil_type: str,  # 參數二：傳入要計算的油品種類（字串，例如 '95' 或 '柴油'）
    last_row: dict  # 參數三：傳入最新一期的相關市場數據（字典格式，包含匯率、他國油價等）
) -> float:  # 型別提示：說明此函式最終會回傳一個浮點數（計算後的油價）
    """  # 多行註解開頭，用於說明此函式的核心邏輯
    【優化版】模擬中油亞鄰競爭國最低價限制
    核心邏輯：稅前平價還原 (Tax-Stripping Parity) + 零售價回歸校正  # 解釋計算方式：先扣除他國稅金還原成「稅前價」，再依台灣稅制換算
    """  # 多行註解結尾
    try:  # 嘗試執行以下計算，若發生錯誤會被 except 捕捉，避免程式中斷
        # 1. 取得最新市場匯率與各國代理價格  # 步驟說明
        usd_twd = float(last_row.get('台幣匯率', 0))  # 從字典取出美金兌台幣匯率，若無則預設為 0，並轉為浮點數
        usd_jpy = float(last_row.get('日圓匯率', 0))  # 從字典取出美金兌日圓匯率，若無則預設為 0，並轉為浮點數
        usd_krw = float(last_row.get('韓元匯率', 0))  # 從字典取出美金兌韓元匯率，若無則預設為 0，並轉為浮點數
        jp_local_raw = float(last_row.get('JP_Price_Proxy', 0))   # 取出日本當地的零售油價代理變數
        kr_local = float(last_row.get('KR_Price_Proxy', 0))   # 取出韓國當地的零售油價代理變數

        # --- 資料防呆 ---  # 步驟說明：檢查資料合理性
        # 修正：移除重複的檢查，整合為一次性檢查  # 開發者留下的修改紀錄
        if np.isnan(jp_local_raw) or jp_local_raw <= 0.1 or \
           np.isnan(kr_local) or kr_local <= 0.1 or \
           usd_twd <= 0:  # 判斷日、韓油價或台幣匯率是否為空值(NaN)或數值極小(異常) (\符號用來將程式碼換行)
            return round(pred_price, 1)  # 如果資料有異常無法計算天花板，就直接回傳原本預測的價格（四捨五入到小數點第一位）
            
        # 匯率防呆：修正倒數 (日圓通常是 140-150，若小於 1 代表是 USD/JPY)  # 說明這段修正匯率方向的問題
        if usd_jpy < 1 and usd_jpy > 0: usd_jpy = 1 / usd_jpy  # 如果匯率介於 0~1，表示抓到的可能是日圓兌美金，取倒數轉回美金兌日圓
        if usd_krw < 1 and usd_krw > 0: usd_krw = 1 / usd_krw # 韓元同理，執行相同的倒數修正機制

        # 🔥 [優化 1] 日本零售價回歸校正  # 步驟說明：針對日本資料做額外校正
        JP_ADJUST_FACTOR = CFG.get("asia_adjust_factor", 1.0)  # 從設定檔取得日本價格的調整係數，預設為 1.0 (不調整)
        JP_BASE_COST = 45.0  # 設定一個日本價格的基礎常數成本（可能由歷史資料回歸得出）
        jp_local = (jp_local_raw * JP_ADJUST_FACTOR) + JP_BASE_COST  # 將原始日本代理價格乘上係數並加上常數，校正為更準確的當地零售價

        # ==========================================
        # 2. 定義各國稅務結構常數 (2025 基準)  # 步驟說明：準備計算稅前價所需的稅率資料
        # ==========================================
        # 判斷是否為柴油，否則一律視為汽油 (92/95/98)  # 說明稅率分類邏輯
        is_diesel = (oil_type == '柴油')  # 判斷油品種類是否為'柴油'，將布林值(True/False)存入變數
        
        TW_EXCISE = 3.99 if is_diesel else 6.83  # 設定台灣貨物稅：柴油為 3.99 元/公升，汽油為 6.83 元/公升
        TW_FUND = 0.11 # 設定台灣固定規費（如土壤及地下水污染整治費等），約 0.11 元/公升
        TW_VAT = 0.05  # 設定台灣的營業稅率為 5%
        
        JP_FUEL_TAX = 32.1 if is_diesel else 53.8  # 設定日本燃料稅：柴油為 32.1 日圓/公升，汽油為 53.8 日圓/公升
        JP_VAT = 0.10  # 設定日本的消費稅率為 10%
        
        KR_FUEL_TAX = 375.0 if is_diesel else 529.0  # 設定韓國交通稅：柴油約 375 韓元/公升，汽油約 529 韓元/公升
        KR_VAT = 0.10  # 設定韓國的增值稅率為 10%

        # ==========================================
        # 3. 核心算式：還原稅前價 -> 換算台幣 -> 加計台灣稅  # 步驟說明：計算「平價」的核心流程
        # ==========================================
        
        # 🔥 [優化 2] 匯率平滑機制  # 步驟說明：避免匯率單日劇烈波動影響油價計算
        ma5_usd = float(last_row.get('MA5_USD_TWD', usd_twd))  # 取得台幣匯率的 5 日移動平均值，若無則用當日匯率代替
        usd_twd_smooth = (usd_twd + ma5_usd) / 2  # 將當日匯率與 5 日均價平均，計算出平滑後的台幣匯率

        # --- 計算日本平價 (Japan Parity) ---  # 開始計算日本的還原油價
        jp_pre_vat = jp_local / (1 + JP_VAT)  # 步驟 1：日本當地售價除以 (1 + 日本營業稅)，推算出售價的「未含營業稅」價格
        jp_base_price = jp_pre_vat - JP_FUEL_TAX  # 步驟 2：再扣除日本的固定燃料稅，得到純粹的「基礎油價 (日圓)」
        jp_base_twd = (jp_base_price / usd_jpy) * usd_twd_smooth  # 步驟 3：先除以美金兌日圓換成美金，再乘上台幣匯率換算成「台幣基礎油價」
        jp_twd_parity = (jp_base_twd + TW_EXCISE + TW_FUND) * (1 + TW_VAT)  # 步驟 4：將台幣基礎油價加上台灣的貨物稅與規費，最後乘上台灣 5% 營業稅，算出若依日本成本在台灣賣的「日本平價」

        # --- 計算韓國平價 (Korea Parity) ---  # 開始計算韓國的還原油價 (邏輯同日本)
        kr_pre_vat = kr_local / (1 + KR_VAT)  # 推算出韓國售價的未含營業稅價格
        kr_base_price = kr_pre_vat - KR_FUEL_TAX  # 扣除韓國固定稅額，得到基礎油價 (韓元)
        kr_base_twd = (kr_base_price / usd_krw) * usd_twd_smooth  # 透過美金作為橋樑，將韓元基礎油價換算為台幣基礎油價
        kr_twd_parity = (kr_base_twd + TW_EXCISE + TW_FUND) * (1 + TW_VAT)  # 加上台灣國內稅費，算出若依韓國成本在台灣賣的「韓國平價」

        # 4. 定義亞鄰天花板  # 步驟說明：決定最高售價限制
        ceiling = np.nanmin([jp_twd_parity, kr_twd_parity])  # 取日本平價與韓國平價兩者中的「最小值」作為亞鄰最低價天花板（忽略NaN值）

        # 5. 合理性檢查 (汽油通常在 20~40 之間)  # 步驟說明：防止算出來的天花板價格過度離譜
        if not (10 < ceiling < 80):  # 檢查天花板價格是否落在合理範圍 (大於 10 且小於 80 元)
            return round(pred_price, 1)  # 若不合理（可能是資料錯誤），放棄天花板限制，回傳原始預測價

        # 6. 執行觸頂判定 (修正：使用 round 統一輸出格式)  # 步驟說明：最後的比對階段
        if pred_price > ceiling:  # 如果模型預測的價格「高於」算出來的亞鄰天花板
            return round(ceiling, 1)  # 政策啟動！只能賣天花板價格，將天花板四捨五入後回傳

    except Exception as e:  # 捕捉 try 區塊內發生的任何錯誤 (例如字典缺值或計算錯誤)
        print(f"⚠️ Asia ceiling calculation error: {e}")  # 在終端機印出錯誤訊息，方便除錯
        
    return round(pred_price, 1)  # 如果預測價沒超過天花板，或是計算過程中發生錯誤，最終都回傳四捨五入後的原始預測價

def apply_smoothing(diff: float, oil_type: str) -> float:  # 定義函式，用來模擬中油的「油價平穩雙重緩衝機制」
    """  # 多行註解開頭
    模擬中油平穩措施 (雙重緩衝機制)
    
    【重要修正】：
    中油的平穩措施實際上是基於「95無鉛汽油的絕對價格」來觸發的，  # 解釋現實世界的真實規則
    而非單純看「漲跌幅 (diff)」。  # 指出現實與程式碼實作上的差異
    但因為這裡傳入的是 diff (漲跌幅)，為了不破壞您的 main 函式結構，  # 說明為何採取妥協的寫法
    這裡維持您的「漲幅級距」邏輯，但請注意這是一個簡化的模擬。  # 提醒使用者目前的實作方式
    """  # 多行註解結尾
    
    # 0. 柴油與汽油同步適用
    # (現實中柴油也是看95價格來決定是否啟動平穩，所以邏輯相同)  # 說明為何不針對柴油做額外判斷
    
    # 1. 跌價或持平 (Diff <= 0) -> 不吸收，直接反應  # 狀況一：降價時
    if diff <= 0:  # 如果價格變動量（漲幅）小於等於 0，代表降價或不變
        return round(diff, 1)  # 中油不吸收降幅，直接將原變動量四捨五入回傳
    
    # 2. 第一階段吸收 (假設對應到漲幅較小的情況)  # 狀況二：小幅上漲時
    # 您的設定：0 ~ 0.25 元 -> 全數反映  # 開發者的原設定邏輯
    if diff <= 0.25:  # 如果漲幅在 0.25 元（含）以內
        return round(diff, 1)  # 全數由消費者負擔，直接回傳原漲幅
    
    # 3. 第二階段吸收  # 狀況三：中度上漲時
    # 您的設定：0.25 ~ 0.5 元 -> 超過 0.25 的部分吸收 25% (民眾負擔 75%)
    # 公式：0.25 + (X - 0.25) * 0.75  # 註解說明計算公式
    elif 0.25 < diff <= 0.5:  # 如果漲幅大於 0.25 且小於等於 0.5 元
        final_diff = 0.25 + (diff - 0.25) * 0.75  # 0.25的部分全額反映，超過0.25的部分只反映75% (民眾吸收)
        return round(final_diff, 1)  # 回傳平穩機制介入後的最終漲幅
    
    # 4. 第三階段吸收  # 狀況四：大幅上漲時
    # 您的設定：> 0.5 元 -> 超過 0.5 的部分吸收 50% (民眾負擔 50%)
    else:  # 如果漲幅超過 0.5 元
        # 第一段滿載: 0.25  # 計算累計負擔拆解說明
        # 第二段滿載: (0.5 - 0.25) * 0.75 = 0.1875  # 第二級距民眾需負擔的最大額度
        base_part = 0.4375 # (0.25 + 0.1875)  # 漲幅達到 0.5 時，民眾總共需負擔的基本額度
        
        final_diff = base_part + (diff - 0.5) * 0.5  # 基本額度 0.4375 加上超過 0.5 元部分的 50% (民眾吸收)
        return round(final_diff, 1)  # 回傳平穩機制大幅介入後的最終漲幅

def compute_decision_threshold(  # 定義函式，用來計算歷史價格的波動門檻
    price_series,  # 參數一：傳入一段歷史價格的資料序列 (pandas Series)
    q=0.7,  # 參數二：設定要取的分位數值，預設為 0.7 (第 70 百分位)
    scale=1.0  # 參數三：設定結果的縮放比例，預設為 1.0 (不縮放)
) -> float:  # 型別提示：回傳計算後的門檻值（浮點數）
    """  # 多行註解開頭
    動態歷史波動門檻  # 說明此函式用途，主要是依據歷史波動大小來設定一個動態標準
    """  # 多行註解結尾
    diffs = price_series.diff().abs().dropna()  # 計算價格序列相鄰兩筆的差值(diff)，取絕對值(abs)後去除空值(dropna)
    if len(diffs) == 0:  # 檢查計算出來的差值序列是否為空（可能傳入的歷史資料不足）
        return 0.5   # 如果資料不足，回傳預設的門檻值 0.5
    th = np.nanpercentile(diffs, q * 100)  # 使用 numpy 計算差值序列中的第 q*100 百分位數 (例如 70%) 作為基礎門檻
    return th * scale # 修正：這裡應該要回傳乘上 scale 後的值  # 將算出的基礎門檻乘上縮放係數 scale 後回傳